public class ArithemeticOperator {
    public static void main(String[] args) {
        
    
    int a=144;
    int b=120;
    
    System.out.println(a+b);

    System.out.println(a-b);

    System.out.println(a*b);

    System.out.println(a/b);

    System.out.println(a%b);
   
int x=1484224;
System.out.println(x%10);
System.out.println(x%100);
System.out.println(x%1000);
}
}
