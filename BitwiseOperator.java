public class BitwiseOperator {
    public static void main(String[] args) {
        int a=28;
        int b=13;
        System.out.println(a^b);
    
        int c=4;
        System.out.println(~c);


        int d=5;
        int e=11;
        System.out.println(d&e);

        int m=10;
        int n=9;
        System.out.println(m|n);
    }
    
}
